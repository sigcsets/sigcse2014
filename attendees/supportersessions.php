THURSDAY   MARCH 6, 2014         10:45 AM - 11:20 PM
Title:  The Engagement and Retention of Students in Undergraduate Computer Science Programs 
Speakers:  Leslie Yeh Johnson, Lecia Barker
Abstract: 
The 2013 Taulbee report shows the fifth straight year of increased CS enrollment rates, this year at 26.9%. New initiatives are driving further growth and broader interest from women and underrepresented minorities. Retaining these students is important for the economy, national security, and equity. Research on retention in CS shows that meaningful and relevant examples used in class, assignments, and curriculum are the strongest predictor of retention. Research also shows that students with limited programming experience, a group to which a majority of women recruits belong, are at greatest risk of leaving CS after the first year. Additionally, the incoming CS students will have a broad set of interests due to the importance of computation in a rapidly expanding number of fields. Providing learning materials that are especially engaging to inexperienced students will be important for retention, and effective scaling strategies will ensure that all interested students can participate in CS programs. 
Come join an introduction to Google’s new program, Engage CSed.  
Engage CSed aims to 
Retain Computer Science undergraduates. 
Prepare for influx of undergraduate Computer Science students. 
Participants will learn how we plan to achieve these aims by providing open content for introductory sequence courses that is effective in engaging and retaining all students, including women. We also plan to support scaling strategies designed to meet the educational needs of a growing Computer Science student population.  We look forward to hearing from educators working on these issues. 

II.    THURSDAY   MARCH 6, 2014         11:25 AM - 12:00 PM 
Title:   CS First 
Speakers:  Kate Berrio, Matthew Dawson and Jamie Sue Goodman
Abstract:  CS First provides teachers and volunteers with the curriculum and supporting materials to lead a CS program focused on building student courage and confidence while also instilling curiosity. Students jump into new computing experiences, develop perseverance and learn the debugging mindset while exposed to basic coding concepts using tools including Scratch and App Inventor. The curriculum is designed for 4th-12th grade students with no programming experience. Session participants will learn how Google is working with both teachers and non-teacher volunteers within South Carolina schools to scale this pilot program.  Participants will also gain access to teacher developed resources that they can bring back to their classroom or share with others.

Friday, March 7  1:45 PM – 3:00 PM 


III.    FRIDAY,  MARCH 7, 2014    1:45 - 3:00 PM
           Title:  Disruptive Innovation in CS Education
   Speakers:   Maggie Johnson, Jane Margolis and Hadi Partovi
High school and college students have been studying computer science for 50 years (e.g., see http://www.iit.edu/csl/cs/about/pdf/high_school_students_use_computers1964.pdf), but this year marks what looks to be a huge and important inflection point in computer science education. In this panel session four people who have each played a significant role as disruptive innovators for computer science education discuss how their ideas and work have been combined with those of the entire community to position us for momentous and important change.
We will hear from each of them about their goals, visions, and aspirations for computer science education; what each is working on as part of realizing these; and how they see their contributions meshing with the community working toward a bright, near-term future.

<h3>Microsoft Supporter Session</h3>

Friday, March 7, 2014 –  10:45 AM- 12:00 Noon
TouchDevelop: The way in which we interact with computing devices is changing: instead of keyboards, advanced touchscreens become more common; mobile devices are often equipped with more sensors, such as location information and acceleration, and are always connected to the cloud.  TouchDevelop is a programming environment and language built around this new reality. Its typed, structured programming language is built around the idea of only using a touchscreen as the input device to author code.
Speaker: Peli de Halleux, Microsoft Research


<h3>GitHub Supporter Session: Saturday, 10:45 AM – 12:00 PM</h3>


Session Title:
Version Control with GitHub: A foundation of collaboration

Speaker Name:
Matthew McCullough

Affiliation:
Trainer, GitHub

Abstract:
In the domain of computer science education, there is a challenge of what can be fit into a given degree program. The inclusion is often ranked by the potential value of a topic as an underpinning of future CS research, computing skills, required employment knowledge, and helpfulness during a student's university years. Version control is a domain that has benefits in all those aforementioned categories, and is evolving from a mere tracking of changes to the fundamental platform on which software collaboration happens at both small and large scale.

In these 70 minutes of exposition and live demos, come see what Git, a modern version control system, brings to a daily software development workflow. Matthew will show how, with the adoption of best practices like solid tests and continuous integration, a third layer of software quality can be obtained by having a polished history of commits, atomically grouping related work, and in an order that makes later review of the code base much easier under production-system duress.

Matthew will tie everything together with a look at how these features are a platform for collaborative light weight code review, starting from a unique "README-driven-development" approach, and flowing all the way through to pushing to in-house or cloud-based production systems.


